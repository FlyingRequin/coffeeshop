export function directionsUrl(activity) {
  const dest = activity.mapQuery || `${activity.name}, Berlin`;
  return `https://www.google.com/maps/dir/?api=1&destination=${encodeURIComponent(dest)}&travelmode=transit`;
}

export function todayDayIndex(now = new Date()) {
  const iso = [
    now.getFullYear(),
    String(now.getMonth() + 1).padStart(2, '0'),
    String(now.getDate()).padStart(2, '0'),
  ].join('-');
  return DAYS.findIndex((d) => d.date === iso);
}
